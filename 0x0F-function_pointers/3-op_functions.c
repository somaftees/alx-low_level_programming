#include "3-calc.h"
/**
 * op_add - add function
 * @a: first num
 * @b: second num
 * Return: sum..
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_mod - a mod function
 * @a: integer.
 * @b: integer.
 * Return: mod..
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_div - a div function
 * @a: integer.
 * @b: integer.
 * Return: div..
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mul - a mul function
 * @a: integer.
 * @b: integer.
 * Return: mult..
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_sub - a sub function
 * @a: integer.
 * @b: integer.
 * Return: sub.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
